# Создаём переменные с нужными данными
completed_tasks = 12  # Количество выполненных домашних заданий (завершенные задачи)
spent_hours = 1.5  # Количество затраченных часов (потраченные часы)
course_name = 'Python'  # Название курса (название курса)

# Вычисляем время на одно задание
time_per_task = spent_hours / completed_tasks
# время на выполнение одной задачи = потраченные часы / выполненные задачи

# Выводим строку с использованием переменных
print(
    f"Курс: {course_name}, всего задач: {completed_tasks}, "
    f"затрачено часов: {spent_hours}, "
    f"среднее время выполнения {time_per_task:.3f} часа."
)
# Основные моменты:
# 1. Названия переменных:
#  - Использован змеиный регистр (snake_case), что соответствует лучшим практикам Python.
#  - Переменные имеют осмысленные имена: completed_tasks, spent_hours, course_name, time_per_task.
# 2. Форматирование строки:
#  - Использованы f-строки для компактного и читабельного вывода.
#  - Точное значение среднего времени (0.125) отображается с тремя знаками после запятой (:.3f).
# 3. Вычисления:
#  - Время на одно задание вычисляется делением spent_hours на completed_tasks.
#  - Используется плавающее деление, что естественно для Python (результат — тип float).
# 4. Читаемость:
#  - Разделение строки вывода на несколько строк в коде (для удобства разработки), но вывод остаётся цельным.